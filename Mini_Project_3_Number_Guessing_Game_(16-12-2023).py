Easy_Level_Turns=15
Medium_Level_Turns=10
Hard_Level_Turns=5


def check_answer(guess, answer, turns):
  """checks answer against guess, returns the number of turns remaining"""
  while turns>0:
    if guess>answer:
      print("Too high")
      return turns-1
    elif guess<answer:
      print("Too low")
      return turns-1
    elif guess==answer:
      print(f"Correct! The number I guessed was {answer}")
      break

def set_difficulty():
  level=input("Choose your difficulty level. Type 'easy', 'medium' or 'hard':").lower()
  global turns
  if level=="easy":
    return Easy_Level_Turns
  elif level == "medium":
    return Medium_Level_Turns
  elif level=="hard":
    return Hard_Level_Turns

def game():
  print("Welcome to the number guessing game!")
  print("I am thinking of a number between 1 and 100.")
  answer=random.randint(1,100)
  turns=set_difficulty()
  guess=0
  while guess!=answer and turns!=0:
    print(f"You have {turns} attempts remaining to guess the number.")
    guess=int(input("Enter your guess number:"))
    turns=check_answer(guess, answer, turns)
    if turns==0:
      print(f"You've lost! The number I guessed was {answer}.")

game()
